	<h3>Cpasmafaute</h3>
	<p>
		Cpasmafaute est l'outil qui fait la spécifité et l'originalité de notre site. A l'origine, nous étions parti-e-s pour créer un site web uniquement dédié à cet outil, mais pour satisfaire les envies de chacun-e-s des créateur-ice-s nous avons ajouté la possibilité de s'entraider par les annonces, ce qui finalement est une grosse proportion du site .. ;) <br/>

