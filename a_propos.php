		Afin de nourrir cet outil, tu as, en tant qu'utilisateur inscrit, la possibilité de proposer une excuse, testée ou non, en indiquant le contexte dans lequel elle pourrait être utilisée adéquatement. <br/>

		Lorsque tu as utilisé une excuse, tu peux la retrouver dans ton compte, et ainsi la noter et la commenter une fois testée. Sympa non ? <br/>

